Ocean::Ocean() {
	renderer = new StaticMeshRenderer(staticMeshNS::reference(staticMeshNS::OCEAN));
	renderer->disableLight();
	initialize();
};

//===================================================================================================================================
//yƒfƒXƒgƒ‰ƒNƒ^z
//===================================================================================================================================
Ocean::~Ocean() {
	SAFE_DELETE(object);
	SAFE_DELETE(renderer);
};

//===================================================================================================================================
//y‰Šú‰»z
//===================================================================================================================================
void Ocean::initialize() {
	object = new Object();
	object->initialize(&D3DXVECTOR3(0, 0, 0));
	object->scale = D3DXVECTOR3(20000, 1, 20000);
	renderer->registerObject(object);
	needUpdate = true;
};

//===================================================================================================================================
//yXVz
//===================================================================================================================================
void Ocean::update()
{
	if (!needUpdate)return;
	object->update();
	renderer->update();
	needUpdate = false;
}

//===================================================================================================================================
//y•`‰æz
//===================================================================================================================================
void Ocean::render(D3DXMATRIX view, D3DXMATRIX projection, D3DXVECTOR3 cameraPositon)
{
	renderer->render(*shaderNS::reference(shaderNS::INSTANCE_STATIC_MESH), view, projection, cameraPositon);
}
